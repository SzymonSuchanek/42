#include <unistd.h>

int	main(void)
{
	int	a;
	int	b;

	a = 9;
	while (a > -1)
	{
		b = a + '0';
		write(1, &b, 1);
		a--;
	}
	write(1, "\n", 1);
	return (0);
}
