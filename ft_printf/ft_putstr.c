#include "libftprintf.h"

size_t	ft_putstr(const char *s)
{
	size_t	i;

	i = 0;
	if (!s)
		return (0);
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		i++;
	}
	i = 0;
	while (s[i] != '\0')
		i++;
	return (i);
}
